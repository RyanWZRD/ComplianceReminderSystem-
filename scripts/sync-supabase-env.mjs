import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, "..");
const envPath = join(root, ".env");
const outPath = join(root, "js", "data", "supabase-env.js");

/**
 * @param {string} content
 * @param {string} key
 * @returns {string}
 */
function readEnvValue(content, key) {
  const pattern = new RegExp(`^${key}\\s*=\\s*(.*)$`, "m");
  const match = content.match(pattern);

  if (!match) {
    return "";
  }

  let value = match[1].trim();

  if (
    (value.startsWith('"') && value.endsWith('"')) ||
    (value.startsWith("'") && value.endsWith("'"))
  ) {
    value = value.slice(1, -1);
  }

  return value;
}

let envContent = "";

try {
  envContent = readFileSync(envPath, "utf8");
} catch (error) {
  if (error && typeof error === "object" && "code" in error && error.code === "ENOENT") {
    console.error("Missing .env file. Copy .env.example to .env and set SUPABASE_URL and SUPABASE_ANON_KEY.");
    process.exit(1);
  }

  throw error;
}

const supabaseUrl = readEnvValue(envContent, "SUPABASE_URL");
const supabaseAnonKey = readEnvValue(envContent, "SUPABASE_ANON_KEY");
const stagingHostsRaw = readEnvValue(envContent, "STAGING_APP_HOSTNAMES");
const stagingHostnames = stagingHostsRaw
  ? stagingHostsRaw
      .split(",")
      .map((host) => host.trim())
      .filter(Boolean)
  : [];

if (!supabaseUrl || !supabaseAnonKey) {
  console.error(
    ".env must define non-empty SUPABASE_URL and SUPABASE_ANON_KEY (see .env.example)."
  );
  process.exit(1);
}

const fileBody = `/**
 * Generated by npm run sync-env — do not edit by hand.
 * Source: .env
 */

/** @type {string} */
export const SUPABASE_URL = ${JSON.stringify(supabaseUrl)};

/** @type {string} */
export const SUPABASE_ANON_KEY = ${JSON.stringify(supabaseAnonKey)};

/** Hostnames allowed for ?cloudWrites=1 (staging only — never production). */
export const STAGING_APP_HOSTNAMES = ${JSON.stringify(stagingHostnames)};
`;

writeFileSync(outPath, fileBody, "utf8");
console.log(`Wrote ${outPath}`);
